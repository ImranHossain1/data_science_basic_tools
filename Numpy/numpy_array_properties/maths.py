import numpy as np
arr = np.array([10, 20, 30])
print(arr + 5)  # Adding 5 to each element of the array
print(arr * 2)  # Multiplying each element by 2
print(arr - 10)  # Subtracting 10 from each element of the array
print(arr / 2)  # Dividing each element by 2
print(arr ** 2)  # Squaring each element of the array
