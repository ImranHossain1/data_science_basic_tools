"""
array[index] #1d array
array[index1, index2] #2d array
array[index1, index2, index3] #3d array
"""

import numpy as np
arr = np.array([10, 20, 30, 40, 50])
print(arr[0]) # Accessing the first element of a 1D array
print(arr[1]) # Accessing the second element of a 1D array
print(arr[2]) # Accessing the third element of a 1D array
print(arr[-1]) # Accessing the last element of a 1D array
